    private void BDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BDeletarActionPerformed
        int resposta = JOptionPane.showConfirmDialog(this,"voê realmente deseja apagar o usuario selecionado?","Apagar",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if (resposta == JOptionPane.YES_OPTION)
        {
            int linhaApagada = tblUsuarios.getSelectedRow();
            modelo.removeRow(linhaApagada);
            limparCampos();
        }
    }//GEN-LAST:event_BDeletarActionPerformed

    private void BAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAtualizarActionPerformed
        int linhaSelecionada = tblUsuarios.getSelectedRow();
        if(linhaSelecionada <= -1)
        {
            JOptionPane.showMessageDialog(null,"Selecione um usuario para ser selecionado");
        }else
        {
            String nome = txtNome.getText();
            String idade = txtIdade.getText();
            String cidade = txtCidade.getText();
            String CPF = txtCPF.getText();
            tblUsuarios.setValueAt(nome, linhaSelecionada, 0);
            tblUsuarios.setValueAt(idade, linhaSelecionada, 1);
            tblUsuarios.setValueAt(cidade, linhaSelecionada, 2);
            tblUsuarios.setValueAt(CPF, linhaSelecionada, 3);
        }
    }//GEN-LAST:event_BAtualizarActionPerformed

