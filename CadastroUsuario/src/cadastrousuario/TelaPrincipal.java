    private void tblUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuariosMouseClicked
        int linhaSelecionada = tblUsuarios.getSelectedRow();
        String nome = (String) tblUsuarios.getValueAt(linhaSelecionada, 0);
        String idade = (String) tblUsuarios.getValueAt(linhaSelecionada, 1);
        String cidade = (String) tblUsuarios.getValueAt(linhaSelecionada, 2);
        String CPF = (String) tblUsuarios.getValueAt(linhaSelecionada, 3);
        
        txtNome.setText(nome);
        txtIdade.setText(idade);
        txtCidade.setText(cidade);
        txtCPF.setText(CPF);
    }//GEN-LAST:event_tblUsuariosMouseClicked

