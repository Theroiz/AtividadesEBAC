    private void BSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BSalvarActionPerformed
            
            String NomeUsuario = txtNome.getText();
            String IdadeUsuario = txtIdade.getText();
            String CPFUsuario = txtCPF.getText();
            String CidadeUsuario = txtCidade.getText();
        if(!isCamposValidos(NomeUsuario,IdadeUsuario,CPFUsuario,CidadeUsuario))
        {
           JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        }else
        {
            Cliente cliente = new Cliente(NomeUsuario,IdadeUsuario,CidadeUsuario,CPFUsuario);
            modelo.addRow(new Object[]{cliente.getNome(),cliente.getIdade(),cliente.getCidade(),cliente.getCpf()});
            limparCampos();
        }


    }//GEN-LAST:event_BSalvarActionPerformed

